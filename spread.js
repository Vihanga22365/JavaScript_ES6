//------------------------------------------------------
//---------------------- Spread Operator with array -----------------------
//------------------------------------------------------

let a = [4, 5, 8, 9];
let b = [6, 5, 7];

console.log(a);
console.log(...a);
console.log(b);
console.log(...b);

let c = [...a, ...b];
console.log(c);

console.log('--------------------------------------------');
//------------------------------------------------------
//---------------------- With word -----------------------
//------------------------------------------------------

let sub = 'Science';

console.log(...sub);

console.log('--------------------------------------------');
//------------------------------------------------------
//---------------------- With object -----------------------
//------------------------------------------------------

let marks = {
  science: 80,
  maths: 65,
};

let self = {
  name: 'Vihanga',
  age: 25,
  ...marks,
};

console.log(marks);
console.log(self);

console.log('--------------------------------------------');
