//------------------------------------------------------
//---------------------- Normal Forloop -----------------------
//------------------------------------------------------

const cities = ['Maharagama', 'Kottawa', 'Mathugama', 'Anuradhapura'];

for (let i = 0; i < cities.length; i++) {
  console.log(cities[i]);
}

console.log('--------------------------------------------');
//------------------------------------------------------
//---------------------- For of Loop -----------------------
//------------------------------------------------------

const subjects = ['Science', 'Maths', 'English', 'Music'];

for (let a of subjects) {
  console.log(a);
}

console.log('--------------------------------------------');
//------------------------------------------------------
//---------------------- For of Loop with entries() method -----------------------
//------------------------------------------------------

const vehicle = ['Bus', 'van', 'Car', 'Bike'];

for (let b of vehicle.entries()) {
  console.log(b);
}
